-- 修正存储过程名
DELIMITER //
CREATE PROCEDURE if not exists drop_homework_tables(IN n INT)
BEGIN
    DECLARE i INT DEFAULT 1;
    WHILE i <= n DO
            SET @sql = CONCAT('DROP TABLE IF EXISTS homework_score', i);
            PREPARE stmt FROM @sql;
            EXECUTE stmt;
            DEALLOCATE PREPARE stmt;
            SET i = i + 1;
        END WHILE;
END //
DELIMITER ;
-- 调用存储过程，删除 homework_score1 到 homework_score20
CALL drop_homework_tables(20);

-- 不需要SUPER权限的版本
DELIMITER $$
DROP PROCEDURE IF EXISTS CreateHomeworkScoreTables$$
CREATE PROCEDURE CreateHomeworkScoreTables()
BEGIN
    DECLARE i INT DEFAULT 1;
    DECLARE done INT DEFAULT 0;

    -- 使用REPEAT循环替代WHILE，减少预处理语句使用
    REPEAT
        SET @create_sql = CONCAT(
                'CREATE TABLE IF NOT EXISTS homework_score', i, ' (',
                '    student_id VARCHAR(20) PRIMARY KEY,',
                '    student_name VARCHAR(50) NOT NULL,',
                '    total_score DECIMAL(5,2) DEFAULT 0.00',
                ') ENGINE=InnoDB DEFAULT CHARSET=utf8mb4'
                          );

        -- 直接执行，减少预处理语句生命周期
        PREPARE dynamic_stmt FROM @create_sql;
        EXECUTE dynamic_stmt;
        DEALLOCATE PREPARE dynamic_stmt;

        SET i = i + 1;
        IF i > 20 THEN
            SET done = 1;
        END IF;
    UNTIL done END REPEAT;

    SELECT '20张作业成绩表创建完成' AS result;
END$$
DELIMITER ;

-- 执行存储过程
CALL CreateHomeworkScoreTables();

